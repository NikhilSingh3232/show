const arr=[4,2,9,"apple","fruits",6];
console.log(arr);

//Modify Element
arr[1]=5
console.log(arr);

// add element
arr.push(12);// Add Element last
arr.unshift(90);// Add Element begining
console.log(arr);

// Remove element
arr.pop(); // remove Element Last
arr.shift();// Remove Element Begining
console.log(arr);